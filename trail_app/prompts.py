    "   查某天（YYYY-MM-DD）的所有工作日志，按 task 分组（带任务标题/状态/性质）。"
    "phase 可选 main/maintenance。**适合'今日/今天/昨天工作内容'类问题**。\n\n"
    "3. list_recent_logs(task_id, since_days?, limit?, phase?)\n"
    "   查某任务的近期工作日志。since_days 默认 30，limit 默认 5 最大 20，"
    "phase 可选 main/maintenance。content 字段会被截断到 800 字。\n\n"
