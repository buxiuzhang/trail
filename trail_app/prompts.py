"""Prompt 集中管理（M3）。

设计原则：
- 短而明确，避免长篇 system prompt
- 用户数据（任务描述 / 日志内容）通过 f-string 拼到 user 段
- 不在 system 段塞业务规则——业务规则靠应用层校验
"""
from __future__ import annotations

from textwrap import dedent


