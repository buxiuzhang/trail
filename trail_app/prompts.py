# ============================================================
# 询问是否进入维护阶段
# ============================================================

ASK_MAINTENANCE_SYSTEM = (
    "你是任务收尾助手。根据用户给出的工作日志整体特征，判断"
    "（仅作为建议，最终由用户决定）：\n"
    "- 任务是否长期存在（还可能偶发出现）\n"
    "- 是否需要后续小修小补\n"
    "用 1-2 句中文回答。给出明确建议（'建议进入维护期' 或 '建议直接关闭'）"
    "并附一句理由。不要 Markdown 包裹。"
)

ASK_MAINTENANCE_USER = (
    "任务「{title}」当前状态：{status}。主体阶段日志：\n\n"
    "{logs}\n\n"
    "是否建议进入维护期？"
)


def build_prompt(template: str, **kwargs) -> str:
    """填模板的 helper。缺失变量报 KeyError（写错时早发现）。"""
    return dedent(template).format(**kwargs)
